type ResultInfo struct {
	Page       int `json:"page"`
	PerPage    int `json:"per_page"`
	Count      int `json:"count"`
	TotalCount int `json:"total_count"`
	TotalPages int `json:"total_pages"`
}

type ListRecordsResponse struct {
	Records    []Record   `json:"result"`
	Success    bool       `json:"success"`
	Errors     []any      `json:"errors"`
	Messages   []any      `json:"messages"`
	ResultInfo ResultInfo `json:"result_info"`
}

func (d *CloudflareDNS) GetTXTRecords(ctx context.Context, record string) (Record, error) {
	url := fmt.Sprintf("%s/zones/%s/dns_records?name.exact=%s", d.config.CloudflareBaseURL, d.config.CloudflareZoneID, record)
	method := "GET"

	client := &http.Client{}
	req, err := http.NewRequest(method, url, nil)
	if err != nil {
		return Record{}, err
	}
	req.Header.Add("Authorization", fmt.Sprintf("Bearer %s", d.config.CloudflareAPIToken))

	res, err := client.Do(req)
	if err != nil {
		return Record{}, err
	}
	defer res.Body.Close()

	body, err := io.ReadAll(res.Body)
	if err != nil {
		return Record{}, err
	}
	var listRecordsResponse ListRecordsResponse
	err = json.Unmarshal(body, &listRecordsResponse)
	if err != nil {
		return Record{}, err
	}
	if len(listRecordsResponse.Records) == 0 {
		return Record{}, fmt.Errorf("no records found for %s", record)
	}
	return listRecordsResponse.Records[0], nil
}
